function normalizeBodyStarKey(starName?: string | null): BodyStarKey | null {
  const key = (starName ?? "").trim();
  if (!key) return null;
  if (key in BODY_STAR_SENTENCE_LIBRARY) return key as BodyStarKey;
  return null;
}

function pickBySeed(arr: string[], seed?: number): string {
  if (!arr.length) return "";
  if (seed == null || !Number.isFinite(seed)) return arr[0];
  return arr[Math.abs(Math.floor(seed)) % arr.length];
}

export function pickBodyStarCore(starName?: string | null, seed?: number): string {
  const key = normalizeBodyStarKey(starName);
  if (!key) return "";
  return pickBySeed(BODY_STAR_SENTENCE_LIBRARY[key].core, seed);
}

export function pickBodyStarTension(starName?: string | null, seed?: number): string {
  const key = normalizeBodyStarKey(starName);
  if (!key) return "";
  return pickBySeed(BODY_STAR_SENTENCE_LIBRARY[key].tension, seed);
}

export function pickBodyStarStrategy(starName?: string | null, seed?: number): string {
  const key = normalizeBodyStarKey(starName);
  if (!key) return "";
  return pickBySeed(BODY_STAR_SENTENCE_LIBRARY[key].strategy, seed);
}

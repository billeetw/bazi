function normPalace(p: string): string {
  const s = (p ?? "").trim();
  if (!s) return "";
  return s.endsWith("宮") ? s : s + "宮";
}

const FALLBACK =
  "你真正要練的，是讓命主的方向感、身主的行動方式與身宮的體感場域彼此配合，而不是彼此拉扯。";

/**
 * 產出 s04 第 5 段：一句整合式總結＋一句具體策略；無資料時回傳 fallback。
 */
export function buildS04StrategyIntegrated(
  destinyName: string,
  bodyName: string,
  bodyPalaceKey: string
): string {
  const d = (destinyName ?? "").trim();
  const b = (bodyName ?? "").trim();
  const pal = normPalace(bodyPalaceKey);
  const t1 = d ? (DESTINY_TRAIT[d] ?? "") : "";
  const t2 = b ? (BODY_TRAIT[b] ?? "") : "";
  const p1 = d ? (DESTINY_PHRASE[d] ?? "") : "";
  const p2 = b ? (BODY_PHRASE[b] ?? "") : "";
  const theme = pal ? (PALACE_THEME[pal] ?? PALACE_THEME[pal.replace(/宮$/, "")] ?? "") : "";
  const concrete = pal ? (PALACE_CONCRETE[pal] ?? PALACE_CONCRETE[pal.replace(/宮$/, "")] ?? "") : "";

  if (d && b && t1 && t2 && p1 && p2) {
    const line1 = `你真正要練的，不是壓住${d}的${t1}，也不是削掉${b}的${t2}，而是讓『${p1}』和『${p2}』變成同一套節奏。`;
    if (theme && concrete) {
      return `${line1}尤其在${theme}的議題上，${concrete}`;
    }
    if (theme) {
      return `${line1}尤其在${theme}的議題上，先對齊命主與身主的節奏，再推進，你就比較不會在最有感的地方因急而失準。`;
    }
    return line1;
  }
  if (d && b) {
    return `你真正要練的，是讓${d}的方向感與${b}的行動方式變成同一套節奏，而不是彼此拉扯。${theme ? `尤其在${theme}的議題上，把力氣放在對的順序上。` : ""}`;
  }
  return FALLBACK;
}

function rowToExport(row: Row): Partial<Record<string, StarPalaceWeight>> {
  return { ...row, 事業宮: row["官祿宮"] };
}

function mergePartialIntoBase(
  base: Partial<Record<string, StarPalaceWeight>>,
  partial: Partial<Row>
): Partial<Record<string, StarPalaceWeight>> {
  const m = { ...base };
  for (const k of Object.keys(partial) as (keyof Partial<Row>)[]) {
    const v = partial[k];
    if (v) m[k as string] = v;
  }
  if (m["官祿宮"]) m["事業宮"] = m["官祿宮"];
  return m;
}

/** 主星全十二宮 + 輔煞雜曜（含 事業宮 別名） */
export function buildCompleteStarPalaceWeightsMap(): Record<string, Partial<Record<string, StarPalaceWeight>>> {
  const out: Record<string, Partial<Record<string, StarPalaceWeight>>> = {};
  for (const [star, row] of Object.entries(MAIN_STAR_FULL_WEIGHTS)) {
    out[star] = rowToExport(row);
  }
  for (const [star, partial] of Object.entries(AUX_AND_SHA_STAR_WEIGHTS)) {
    out[star] = mergePartialIntoBase(out[star] ?? {}, partial);
  }
  if (out["截空"] && !out["截路"]) out["截路"] = { ...out["截空"] };
  return out;
}

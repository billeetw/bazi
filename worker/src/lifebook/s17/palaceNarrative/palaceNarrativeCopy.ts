import { PALACES } from "../../schema.js";

function palaceDisplayWithSuffix(name: string): string {
  if (name.endsWith("宮")) return name;
  return `${name}宮`;
}

/** 12 宮通用：本宮與三方四正另三宮（三合兩宮＋對宮）一句，供【相關牽動】預設 */
export function defaultSanfangRelatedNote(palaceCanon: string): string {
  const p = (palaceCanon ?? "").trim();
  if (!p) return "此宮與三方四正諸宮彼此牽動，宜對照全盤一併閱讀。";
  const idx = PALACES.findIndex((m) => palaceDisplayWithSuffix(m.name) === p || m.name === p.replace(/宮$/, ""));
  if (idx < 0) return `${p}與三方四正諸宮彼此牽動，宜對照全盤一併閱讀。`;
  const t1 = (idx + 4) % 12;
  const t2 = (idx + 8) % 12;
  const opp = (idx + 6) % 12;
  const fmt = (i: number) => palaceDisplayWithSuffix(PALACES[i].name);
  const self = fmt(idx);
  const others = [...new Set([fmt(t1), fmt(t2), fmt(opp)])].filter((x) => x !== self);
  return `${self}與${others.join("、")}形成三方四正，彼此牽動。`;
}


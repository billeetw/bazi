function getDestinyCategory(starName: string): DestinyCategory {
  return DESTINY_STAR_TO_CATEGORY[starName] ?? "實作型";
}

function getBodyCategory(starName: string): BodyCategory {
  return BODY_STAR_TO_CATEGORY[starName] ?? "策略型";
}

/**
 * 依命主、身主星名產出「內外對話」敘事一句；先分類再套模板，不露示例與戰略語氣。
 */
export function buildDestinyBodyDialogue(destinyStar: string, bodyStar: string): string {
  const d = getDestinyCategory(destinyStar);
  const b = getBodyCategory(bodyStar);
  const row = DIALOGUE_TEMPLATES[d];
  if (!row) return "";
  const template = row[b];
  if (template) return template;
  return `命主與身主共同形成你「如何使用自己」的慣性：用命主找方向，用身主帶動行動，兩者對齊時最能發揮。`;
}

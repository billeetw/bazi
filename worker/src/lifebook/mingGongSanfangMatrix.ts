/** 可選 content：未命中矩陣時用 starSanfangFamilies[主星].mingPattern */
export function getMingGongSanfangInsight(
  leadStarName: string,
  seed: number,
  content?: { starSanfangFamilies?: Record<string, { mingPattern?: string }> }
): string {
  const star = leadStarName.replace(/\s/g, "");
  const list = MING_GONG_SANFANG_BY_STAR[star] ?? MING_GONG_SANFANG_BY_STAR[leadStarName];
  if (list && list.length > 0) {
    return list[Math.abs(seed) % list.length];
  }
  const mingPattern = content?.starSanfangFamilies?.[star]?.mingPattern ?? content?.starSanfangFamilies?.[leadStarName]?.mingPattern;
  if (mingPattern?.trim()) return mingPattern.trim();
  return MING_GONG_SANFANG_FALLBACK[Math.abs(seed) % MING_GONG_SANFANG_FALLBACK.length];
}

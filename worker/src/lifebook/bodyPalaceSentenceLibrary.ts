function normalizeBodyPalaceKey(palaceKey?: string | null): BodyPalaceKey | null {
  const raw = (palaceKey ?? "").trim();
  if (!raw) return null;
  const withSuffix = raw.endsWith("宮") ? raw : `${raw}宮`;
  if (withSuffix in BODY_PALACE_SENTENCE_LIBRARY) return withSuffix as BodyPalaceKey;
  const aliases: Record<string, BodyPalaceKey> = {
    兄弟: "兄弟宮", 夫妻: "夫妻宮", 子女: "子女宮", 財帛: "財帛宮",
    疾厄: "疾厄宮", 遷移: "遷移宮", 僕役: "僕役宮", 官祿: "官祿宮",
    田宅: "田宅宮", 福德: "福德宮", 父母: "父母宮",
  };
  return aliases[raw] ?? null;
}

function pickBySeed(arr: string[], seed?: number): string {
  if (!arr.length) return "";
  if (seed == null || !Number.isFinite(seed)) return arr[0];
  return arr[Math.abs(Math.floor(seed)) % arr.length];
}

export function pickBodyPalaceCore(palaceKey?: string | null, seed?: number): string {
  const key = normalizeBodyPalaceKey(palaceKey);
  if (!key) return "";
  return pickBySeed(BODY_PALACE_SENTENCE_LIBRARY[key].core, seed);
}

export function pickBodyPalaceTension(palaceKey?: string | null, seed?: number): string {
  const key = normalizeBodyPalaceKey(palaceKey);
  if (!key) return "";
  return pickBySeed(BODY_PALACE_SENTENCE_LIBRARY[key].tension, seed);
}

export function pickBodyPalaceStrategy(palaceKey?: string | null, seed?: number): string {
  const key = normalizeBodyPalaceKey(palaceKey);
  if (!key) return "";
  return pickBySeed(BODY_PALACE_SENTENCE_LIBRARY[key].strategy, seed);
}

  /**
   * 取得流月戰略建議句
   * @param {string} gz - 干支（如 "庚寅"）
   * @param {string} dominant - 十神主軸（如 "比肩"、"七殺"）
   * @param {number} [riskScore] - 風險分數（可選，未來可依風險調整語氣）
   * @returns {string} 戰略建議句
   */
  function getStrategy(gz, dominant, riskScore) {
    const lib = LIUYUE_STRATEGY_LIBRARY[gz];
    if (!lib) return FALLBACK_STRATEGY;

    const shichen = (dominant || "").trim();
    const normalized = SHICHEN_ALIASES[shichen] || shichen;
    const sentence = lib[normalized] || lib.default || FALLBACK_STRATEGY;
    return sentence;
  }

  if (!window.UiUtils) {
    window.UiUtils = {};
  }
  window.UiUtils.LiuyueStrategy = {
    getStrategy,
    FALLBACK_STRATEGY,
  };
